package com.example.eventbooking.model;

public class MoreModel {
    private String title;
    private int image;

    public MoreModel() {
    }

    public MoreModel(String title, int image) {
        this.title = title;
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public int getImage() {
        return image;
    }
}
